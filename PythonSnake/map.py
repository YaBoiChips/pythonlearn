import pygame

TILE_SIZE = 64

# double linked enum
TILE_TYPE = {
    0: "air",
    1: "ground",
    2: "kill",
}

TILE_COLORS = {
    "ground": (255, 255, 255),
    "kill": (255, 0, 0),
}

class Tile:
    _rect = None
    _id = -1
    _pos = pygame.Vector2(0, 0)

    def __init__(self, t_id, pos):
        self._id = t_id
        self._pos = pos
        self._rect = pygame.Rect(pos.x * TILE_SIZE, pos.y * TILE_SIZE, TILE_SIZE, TILE_SIZE)

    def get_rect(self):
        return self._rect

    def get_id(self):
        return self._id

    def get_type(self):
        return TILE_TYPE[self._id]

    def get_color(self):
        return TILE_COLORS[self.get_type()]

    def draw(self, screen):
        pygame.draw.rect(screen, self.get_color(), self._rect)


